#include "libft.h"

void	*ft_memalloc(size_t size)
{
	size_t	i;
	void	*s;

	s = (void*)malloc(size);
	if (s)
	{
		i = 0;
		while (i < size)
		{
			s[i] = 0;
			i++;
		}
	}
	return (s);
}
